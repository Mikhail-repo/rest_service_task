CREATE DATABASE users;
USE users;
CREATE TABLE accounts(Email VARCHAR(50) PRIMARY KEY, FirstName VARCHAR(40), LastName VARCHAR(40), Password VARCHAR(250), Admin BOOLEAN);


CREATE TABLE polls(id INTEGER NOT NULL AUTO_INCREMENT PRIMARY KEY, name VARCHAR(50), startdate TIMESTAMP, enddate TIMESTAMP, description VARCHAR(250));
CREATE TABLE questions(id INTEGER NOT NULL AUTO_INCREMENT PRIMARY KEY, questiontype VARCHAR(50), questiontext VARCHAR(250));

CREATE TABLE joinpolls(
	poll_id INTEGER NOT NULL, 
	question_id INTEGER NOT NULL,
	CONSTRAINT FK_POLL_ID FOREIGN KEY (poll_id) REFERENCES polls (id),
    CONSTRAINT FK_QUESTION_ID FOREIGN KEY (question_id) REFERENCES questions (id)
);
